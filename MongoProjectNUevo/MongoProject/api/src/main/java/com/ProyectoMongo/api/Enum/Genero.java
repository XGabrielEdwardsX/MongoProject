package com.ProyectoMongo.api.Enum;

/**
 * El enumerado Genero representa los géneros posibles que pueden ser asignados
 * a una entidad, como un usuario o un producto. Este enumerado se utiliza para
 * mantener consistencia en los valores de género y evitar errores de validación
 * o de entrada de datos incorrectos.
 */
public enum Genero {
    /**
     * Representa el género masculino.
     */
    Hombre,

    /**
     * Representa el género femenino.
     */
    Mujer;
}
