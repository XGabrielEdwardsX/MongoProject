package com.ProyectoMongo.api.Enum;

/**
 * El enumerado Rol representa los roles posibles que pueden ser asignados
 * a los usuarios en la aplicación. Este enumerado se utiliza para mantener
 * consistencia en los valores de roles y evitar errores de validación
 * o de entrada de datos incorrectos.
 */
public enum Rol {
    /**
     * Representa el rol de cliente.
     */
    Cliente,

    /**
     * Representa el rol de administrador.
     */
    Admin;
}
